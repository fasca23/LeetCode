class Solution:
    def isSameTree(self, p, q) -> bool:
        """
        DFS с явным стеком (в куче).
        
        Почему лучше рекурсии:
        - Рекурсия: стек вызовов ~1000 в Python → упадёт на цепочке из 1001 узла
        - Этот вариант: стек в куче → вмещает миллионы элементов (пока RAM есть)
        - Каждый элемент стека — кортеж из 2 ссылок (~16 байт)
        - 1 млн узлов → ~16 МБ, 10 млн → ~160 МБ — легко
        
        Почему лучше BFS с очередью:
        - BFS хранит целый уровень: O(w) где w — ширина
        - На сбалансированном дереве w ≈ n/2 → миллионы элементов
        - DFS хранит только путь от корня: O(h) где h — высота
        - Максимум h = n (вырожденное), обычно h << n
        
        Итого: DFS со стеком = универсальный вариант.
        Не падает ни на цепочке, ни на широком дереве.
        """
        stack = [(p, q)]
        
        while stack:
            a, b = stack.pop()
            
            if not a and not b:
                continue
            
            if not a or not b or a.val != b.val:
                return False
            
            stack.append((a.left, b.left))
            stack.append((a.right, b.right))
        
        return True
